/*! \file tick.c
 *
 *  \brief Tick implmentation
 *
 *  This modules conts the number of ticks in 1ms
 *  The increments are triggered by a Timer IRQ
 *
 *
 *  \author Gustavo Patricio
 *
 *
 */

/*
******************************************************************************
* INCLUDES
******************************************************************************
*/
#include "tick.h"

/*
******************************************************************************
* LOCAL VARIABLES
******************************************************************************
*/
static uint32_t gTick = 0;

/*
******************************************************************************
* GLOBAL FUNCTIONS
******************************************************************************
*/

/*******************************************************************************/
void tickInc(void)
{
    gTick++;
}

/*******************************************************************************/
uint32_t tickGet(void)
{
    return gTick;
}